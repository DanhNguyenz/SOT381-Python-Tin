Ngày = float(input("Hãy nhập số ngày:"))

if Ngày == 2:
    print("Thứ Hai")
elif Ngày == 3:
    print("Thứ Ba")
elif Ngày == 4:
    print("Thứ Tư")
elif Ngày == 5:
    print("Thứ Năm")
elif Ngày == 6:
    print("Thứ Sáu")
elif Ngày == 7:
    print("Thứ Bảy")
elif Ngày == 8:
    print("Chủ Nhật")
else:
    print("Lỗi!")
